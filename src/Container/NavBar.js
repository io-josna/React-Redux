import React from "react";
import { NavLink } from "react-router-dom";
import "../Styles/NavBar.css";
import Logo from "./Logo";
import ShoppingCartIcon from "@mui/icons-material/ShoppingCart";
import { useSelector } from "react-redux";

const NavBar = () => {
  const numberOfCartItems = useSelector((state) =>
    state.cart.reduce((total, item) => total + item.quantity, 0)
  );

  return (
    <nav className="navbar">
      <div className="list">
        <ul className="nav-links">
          <li className="nav-item">
            <NavLink className="nav-links-style" to="/">
              <Logo />
            </NavLink>
          </li>
          <li className="nav-item">
            <NavLink className="nav-links-style" to="/Products">
              Products
            </NavLink>
          </li>
          <li className="nav-item">
            <NavLink className="nav-links-style" to="/Cart">
              <ShoppingCartIcon />
              <span className="cart-item-count" style={{ width: "20px" }}>
                {numberOfCartItems}
              </span>
            </NavLink>
          </li>
        </ul>
      </div>
    </nav>
  );
};

export default NavBar;

